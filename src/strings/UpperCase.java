package strings;

public class UpperCase {
    public static void main(String[] args) {
        String s1;
        s1 = "HELLO WORLD";
        System.out.println(" before converting to upper case " + s1);
        s1 = s1.toUpperCase();
        System.out.println(" after converting to upper case " + s1);
        System.out.println(s1);
    }

}

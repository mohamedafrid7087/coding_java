package strings;

public class LowerCaseString {
    public static void main(String[] args){
        String s1;
        s1 = "HELLO WORLD";
        System.out.println("before converting to lower case" +s1);
        s1=s1.toLowerCase();
        System.out.println("after comnverting to lower case" +s1);
        System.out.println(s1);

    }
}
